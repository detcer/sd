    public function toggleFavorite( Request $req , $id ) {
        $data = [ 'model_id' => $id , 'user_id' => Auth::user() -> id ];
        $fav = Favorite::where( $data ) -> count();
        if ( ! $fav ) {
            Favorite::create( $data );
        } else {
            Favorite::where( $data ) -> delete();
        }
        return response() -> json();
    }
